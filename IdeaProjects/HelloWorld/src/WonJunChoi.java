public class WonJunChoi {

    // Application의 시작점
    public static void main(String[] args) {

        String name = "최 원준";
        String address = "서울 서초구 반포동";
        String street = "사평대로 154길";
        int streetNumber = 715;

        // "최 원준"을 출력하라.
        System.out.println(name); //글자

        // "서울 서초구 반포동"
        System.out.println(address); //글자

        // "사평대로 154길"
        System.out.println(street); //글자

        // "715"
        System.out.println(streetNumber); //글자 ->""안에 있으면 무조건 글자
        System.out.println(715); //자연수

    }
}
